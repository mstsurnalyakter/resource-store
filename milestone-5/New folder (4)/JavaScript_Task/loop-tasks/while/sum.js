/***
Subtask-1:
Display sum of all the odd numbers from 81 to 131.
 */
let i = 81;
let sum1 = 0;
while(i <= 131){
    sum1 += i;
    i = i + 2;
}

console.log(`Odd number sum1: ${sum1}`);

